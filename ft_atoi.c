#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	s;
	int	sol;

	i = 0;
	s = 1;
	sol = 0;
	while (str[i] == ' ' || str[i] == '\r' || str[i] == '\t' || str[i] == '\n'
			|| str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	while (47 < str[i] && str[i] < 58)
	{
		sol = sol * 10 + (str[i] - 48);
		i++;
	}
	return (sol * s);
}
